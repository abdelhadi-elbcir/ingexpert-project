<p class=MsoNormal>&nbsp;</p>

<h2>A - Pourquoi sous-traiter? </h2>



<p class=MsoNormal><span style='font-family:"Arial","sans-serif"'>Les arguments
pour passer à la sous-traitance peuvent être de plusieurs ordres (en dehors du
gain financier qui reste à démontrer): <br>
<em><span style='font-family:"Arial","sans-serif"'>Attention: le but est de faire
un constat de la situation actuelle et nullement de critiquer les intervenants
de maintenance, c'est bien la conjoncture qui nous amène à la situation décrite.</span></em> </span></p>




<p class="renfoncement1">
<span style="color: orange; "><strong>• </span></strong> La sous-traitance de la maintenance est un moyen de contrôler les dépenses. Plus que le Donneur d'ordres, le sous-traitant doit veiller à la rentabilité du contrat.
<br><span style="color: orange; "><strong>• </span></strong> L'image de la maintenance n'est pas bonne. Le rejet fréquent de la fonction méthodes tend à prouver que le personnel maintenance refuse de progresser. Quel est le service maintenance qui pratique les probabilités ? 
<br><span style="color: orange; "><strong>• </span></strong> Absence fréquente de veille technologique en interne.
<br><span style="color: orange; "><strong>• </span></strong> La technique n'est souvent pas le problème de fond.
<br><span style="color: orange; "><strong>• </span></strong> Le service maintenance a du mal à chiffrer son budget et de fait ne sait pas convaincre sa direction.
<br><span style="color: orange; "><strong>• </span></strong> Les maintenanciers ne sont pas souvent très humbles (mais il est vrai souvent très compétents).
<br><span style="color: orange; "><strong>• </span></strong> Les personnels maintenance préfèrent malheureusement le rôle de pompier (qu'ils maîtrisent mieux que quiconque, et pour cause) aux interventions préventives 
<br><span style="color: orange; "><strong>• </span></strong> le personnel de maintenance n'est pas renouvelé à l'issue des départs (retraite, mutation, etc)

<br> </p>






<h2>B - Les différentes formes de sous-traitance et leurs
rémunérations</h2>



    <h3>1
- Les formes de sous-traitance</h3>
<p class=MsoNormal><span style='font-family:"Arial","sans-serif"'>Les formes de
sous-traitance de la maintenance sont nombreuses. Ce n'est pas la façon
d'intervenir qui change, puisque le travail du technicien reste le même, mais
c'est le cadre juridique de l'intervention qui va définir la forme de sous-traitance.
Aussi, nous pouvons dissocier les interventions en les classant par modes
de rémunération:</span></p>


<p class="renfoncement1">
<span style="color: orange; "><strong>• </span></strong> Intervention
     rémunérée au temps passé: mission limitée dans le temps (régie, travail
     temporaire). La rémunération pourra être de type dépense&nbsp;contrôlée
<br><span style="color: orange; "><strong>• </span></strong> Intervention
     sur devis
<br><span style="color: orange; "><strong>• </span></strong> Forfait
     (peut être partiel: limité à un nombre d'heures, le restant étant de la
     dépense contrôlée plafonnée ou non)
<br><span style="color: orange; "><strong>• </span></strong> Intervention
     rémunérée au bordereau-points
</p>

<p class=MsoNormal><span style='font-family:"Arial","sans-serif"'>Bien entendu la
relation entre le Donneur d'Ordres et le prestataire de maintenance ne devra
jamais entrer dans le cadre d'un délit de marchandage.</span></p>

<p class=MsoNormal><span style='font-family:"Arial","sans-serif"'>Pour cela il
faut que le périmètre d'intervention soit défini et qu'il&nbsp;n'y ai pas
subordination:&nbsp;les équipes intervenantes doivent donc être totalement
indépendantes du donneur d'ordres (donc avoir un responsable désigné) et avoir
une mission parfaitement définie à l'avance. </span></p>

<p class=MsoNormal style='margin-left:144.0pt'><span style='font-family:"Arial","sans-serif"'>&nbsp;</span></p>

    <h3>2
- Contrat de moyen ou de résultat ?</h3>
<p class=MsoNormal><i><span style='font-family:"Arial","sans-serif"'>Notion de
résultat:</span></i></p>

<p><span style='font-family:"Arial","sans-serif"'>Contrairement à la notion de moyen,
la notion de résultat porte l'obligation à un prestataire de maintenance de
réaliser sa prestation en respectant un objectif défini au préalable (ratio, un
nombre d'heures, résultat technique, etc...). La relation est plus confortable
pour le Donneur d'Ordres qui aura su définir des objectifs avant
l'intervention:&nbsp;en cas de litige sur l'objectif, c'est au prestataire de
démontrer qu'il a tout mis en oeuvre pour atteindre l'objectif et non pas au
donneur d'ordres de démontrer la faute.</span></p>

<p><span style='font-family:"Arial","sans-serif"'>Nous encourageons vivement
les contrats à obligation de résultats, mais il n'est pas &nbsp;toujours simple
de définir l'objectif à atteindre (délai imparti, coût, etc.).</span></p>

<p style='margin-left:72.0pt'><span style='font-family:"Arial","sans-serif"'>&nbsp;</span></p>

    <h3>3
- Un exemple de rémunération &quot;mixte&quot; appliquée cas du cost and fee:</h3>
<p class=MsoNormal><span style='font-family:"Arial","sans-serif"'>La
rémunération de type cost and fee peut être couplée à des clauses
d'intéressement destinées à motiver le prestataire de maintenance. </span></p>

<p style='margin-left:72.0pt'><span style='font-family:"Arial","sans-serif"'>&nbsp;</span></p>

<p style='margin-left:72.0pt'><span style='font-family:"Arial","sans-serif"'>Rémunération
= Coûts fixes + Coûts variables&nbsp;+ 0,5*&nbsp;(Valeur Cible - Coûts
variables)</span></p>

<p class=MsoNormal style='margin-left:144.0pt'><span style='font-family:"Arial","sans-serif"'>*
à moduler</span></p>


<p class="renfoncement1">
<span style="color: orange; "><strong>• </span></strong> Coûts fixe&nbsp;= le prestataire est assuré de
       voir ses frais de structure payés.
<br><span style="color: orange; "><strong>• </span></strong> Coûts variables&nbsp;=&nbsp;le prestataire se voit
       rémunérer la partie variable de son intervention (heures) en fonction du
       temps passé.
<br><span style="color: orange; "><strong>• </span></strong> Intéressement : 0,5*&nbsp;(Valeur Cible - Coûts
       variables) = une cible d'heures a été définie, en cas de dépassement le
       prestataire ne touchera pas d'intéressement.
</p>

<br>

<h2>F - Cas particulier: le contrat de maintenance au forfait</h2>

 

<p class=MsoNormal><span style='font-family:"Arial","sans-serif"'>Le contrat de
maintenance de type forfait suscite un engouement tout particulier en ce début
de 21ème siècle. La mode mondiale de maîtrise des coûts fait de ce type de
rémunération l'outil idéal puisque son exploitation permet de définir
clairement les budgets de maintenance.</span></p>

<p class=MsoNormal><span style='font-family:"Arial","sans-serif"'>L'application
du contrat de type forfait est-elle si simple? Malheureusement il n'est pas
rare de constater que des prestataires réclament, au delà du forfait, des
sommes parfois conséquentes (20 % du forfait) au donneur d'ordres. A qui
incombe la faute ? La réponse n'est pas simple, on constate qu'il arrive
souvent que le donneur d'ordres paye les sommes réclamées en plus du forfait.
Le prestataire lui soumet pour cela des éléments de preuves qui justifient ces
dépassements.</span></p>

<p class=MsoNormal><span style='font-family:"Arial","sans-serif"'>On comprend
donc que la rédaction du contrat et le montant du forfait ne doivent pas être
élaborés avec légèreté. Pour arriver à une valeur chiffrée finale (le montant
forfaitaire annuel), un Donneur d'Ordres&nbsp;doit traiter/recouper beaucoup de
chiffres issus des&nbsp;:</span></p>



<p class="renfoncement1">
<span style="color: orange; "><strong>• </span></strong> Périmètres
     géographiques et techniques;
<br><span style="color: orange; "><strong>• </span></strong> Historiques
     travaux (nature, type, urgence, durée).
 
</p>

