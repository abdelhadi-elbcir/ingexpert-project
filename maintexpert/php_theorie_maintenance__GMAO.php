                <h1>GMAO</h1>

                <br>

                <p class="redaction">

                    La Gestion de la Maintenance Assistée par Ordinateur est constituée d’une base de
                    données (historique) qui est alimentée par le personnel de maintenance via un
                    formulaire. Des pannes&nbsp;sont mises en mémoire pour certains équipements
                    (date, temps passé, intervenant, matériel remplacé,
                    <br><br>La base de l’historique est l’inventaire&nbsp;des équipements: appelé découpage fonctionnel.
                    <br><br>
                    Chaque GMAO est personnalisée selon les besoins spécifiques d’exploitation de l’historique ou le
                    fonctionnement d’un site.
                </p>

                <br>

                <h2>A - Des questions à se poser</h2>

                <p class="redaction">Avant de faire l'acquisition d'une GMAO, il est nécessaire de se poser toutes les
                    questions appropriées.</p>

                <p class="renfoncement1">
                    <span style="color: orange; "><strong>• </span></strong> Découpage fonctionnel : jusque sur quelle
                    plus petite partie d'équipement faut-il&nbsp;travailler&nbsp;? ;
                    <br><span style="color: orange; "><strong>• </span></strong> Historiques&nbsp;: jusqu’à quel niveau
                    de panne faut-il aller? Par exemple, faut-il enrichir la GMAO des «&nbsp;micros-pannes&nbsp;»? Tous
                    les équipements sont-ils concernés ? ;
                    <br><span style="color: orange; "><strong>• </span></strong> Etc...
                </p>


                <p class="redaction">

                    Les renseignements fournis à la GMAO seront fonction des informations que l’on souhaite exploiter
                    ultérieurement. Il est donc nécessaire de connaître sa politique de maintenance et plus
                    particulièrement le rôle du service méthodes.

                    <br><br> Des réponses à toutes les questions naîtra la&nbsp;définition de la GMAO, qui sera traduite
                    en cahier des charges destiné à l'acquisition de l'outil approprié.

                </p>

                <br>

                <h2>B - Le plan de maintenance </h2>

                <p class="redaction">Le
                    <a href="https://dev.ingexpert.com/maintexpert/php_theorie_maintenance__plan_de_maintenance.php">plan
                        de maintenance </a>
                    regroupe les intentions d’intervention de maintenance
                    préventive (et non corrective au sens de la normalisation). Il est maintenant
                    couramment intégré à la GMAO. Ainsi ce qui est planifié est rappelé par la GMAO
                    (alarmes). Aux opérateurs ensuite d’informer la GMAO de ce qui a été découvert
                    sur le terrain et ce qui a été réellement fait.
                    <br><br>

                    Il
                    existe des outils pour la constitution du plan (méthodologies, arbres de prises
                    de décision, abaques) mais la base de sa réalisation est liée à l’exploitation des historiques
                    maintenance et des statistiques. <br><br>

                    Il
                    doit être mis à jour dès que le contexte change (taux d’engagement, produit
                    fabriqué, etc…).
                </p>
