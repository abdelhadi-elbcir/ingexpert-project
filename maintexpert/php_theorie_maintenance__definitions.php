                    <h2>B - Maintenabilité</h2>

                    <p class="redaction">Dans des conditions données
                        d'utilisation, aptitude d'un bien à être maintenu ou rétabli dans un état où il
                        peut accomplir une fonction requise, lorsque la maintenance est accomplie dans
                        des conditions données, en utilisant des procédures et des moyens prescrits.
                        &quot; (Extrait norme NF EN 13306 X 60-319)
                        <br><br>

                        C’est la probabilité que la maintenance
                        d’un système S accomplie dans des conditions données, soit effectué sur
                        l’intervalle [0,t] sachant qu’il est défaillant à l’instant t = 0.
                        <br>

                    <h5 style='margin-left:35.4pt'>
                        <font face="Arial"><span
                                style="font-family:Arial,sans-serif; font-size:10pt; line-height:115%;"><b>M(t) = P {S
                                    est réparé sur l’intervalle [0,t]
                                    }</b></span></font>
                    </h5>




                    <p class="redaction">La maintenabilité est conditionnée par la conception de l’équipement&nbsp;:<br>

                        <br>
                        &nbsp;&nbsp;. Outils nécessaires au diagnostic de la panne incorporés ou non et à la réparation
                        (dont dispositif de Maintenance conditionnelle)

                        <br>&nbsp;&nbsp;. Contrôle du bon fonctionnement (points de mesure, afficheurs, etc…)

                        <br>&nbsp;&nbsp;. Documentation appropriée (dont modes opératoires)

                        <br>&nbsp;&nbsp;. Réparation ou mesure en marche (isoler certains circuits, etc)

                        <br>&nbsp;&nbsp;. Accessibilité, démontabilité (détrompeur, repérage, outils communs),
                        interchangeabilité

                        <br>&nbsp;&nbsp;. Manutention simple (potence intégrée, rails, etc…)

                        <br>&nbsp;&nbsp;. etc

                        <br><br><br>


                    <h2>C - Fiabilité</h2>

                    <p class="redaction">Aptitude d'un bien à accomplir
                        une fonction requise, dans des conditions données, durant un intervalle de
                        temps donné. &quot; (Extrait norme NF EN 13306 X 60-319).

