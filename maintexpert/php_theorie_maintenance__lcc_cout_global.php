  <h1>LCC</h1>
	
<p class=MsoNormal><span style='font-family:"Arial","sans-serif"'>&nbsp;</span></p>

<p class=MsoNormal><span style='font-family:"Arial","sans-serif"'>&nbsp;</span></p>

<p class="redaction">
Définition du Cycle de Vie selon la norme EN13306: 
<br>"Série de stades par lesquels passe un bien, de sa conception à sa mise au rebut".
</p>

<p class=MsoNormal><b><span style='font-family:"Arial","sans-serif"'>LCC (Life Cycle Cost) = Coût du cycle de vie</span></b></p>

<p class="redaction">
Voici le genre de question qu'il est possible de résoudre par l'approche du coût du cycle de vie :
<br>Je dois optimiser la maintenance d'un point de vue financier. Pour cela on me demande d'utiliser la méthode LCC, qui est le calcul du coût d'un équipement tout au long de sa vie (parfois appelé vie série). Je dois déterminer, en fonction de son prix, de sa date d'achat, et des actions de maintenance associées, s'il faut continuer d'investir sur un tel matériel ou s'il est préférable de le changer.&quot;</span></p>
</p>


    <h2>A - Définition</h2>
	
<p class="redaction">
Le LCC correspond au coût global de possession. Pour le déterminer, il faut tenir compte des coûts suivants:
</p>

<p class="renfoncement1">
<span style="color: orange; "><strong>• </span></strong>  l'acquisition 
<br><span style="color: orange; "><strong>• </span></strong> l'exploitation 
<br><span style="color: orange; "><strong>• </span></strong>  à la maintenance (y compris coûts indirects) 
<br><span style="color: orange; "><strong>• </span></strong>  à l'élimination du bien 
</p>

<p align=center style='text-align:center'><b><span style='font-family:"Arial","sans-serif"'>LCC
= V + D + C + E</span></b></p>

<p><span style='font-family:"Arial","sans-serif"'>&nbsp;</span></p>

<p class="renfoncement1">
<span style="color: orange; "><strong>• </span></strong>  V = investissement initial (frais d'études, coût de passation commande, frais de logistique, coût de l'équipement)
<br><span style="color: orange; "><strong>• </span></strong>D = dépenses d'exploitation (énergie, consommables, main d'oeuvre)
<br><span style="color: orange; "><strong>• </span></strong>C = coûts de maintenance y compris coûts indirects &nbsp;
<br><span style="color: orange; "><strong>• </span></strong>E = coût d'élimination, prix de revente 
</p>


<p style='margin-left:18.0pt'><i style='mso-bidi-font-style:normal'><span
style='font-family:"Arial","sans-serif"'>Remarque 1: il faut travailler en euros constants en additionnant les coûts sur plusieurs années.</span></i></p>

<p style='margin-left:18.0pt'><i style='mso-bidi-font-style:normal'><span
style='font-family:"Arial","sans-serif"'>Remarque&nbsp;2 : les coûts indirects de maintenance sont constitués de&nbsp;:</span></i></p>



<p class="renfoncement1">
<span style="color: orange; "><strong>• </span></strong>Perte de production (main d’ouvre à l’arrêt, non-qualité, arrêts induits, micro-arrêts ou micro-pannes, mode dégradé, perte matière, perte de confiances, perte de productivité, conséquence sur les ventes, cout de non sécurité, etc)
<br><span style="color: orange; "><strong>• </span></strong>Pénalités
<br><span style="color: orange; "><strong>• </span></strong>Auto-maintenance
 </p>
 
 
 
 

