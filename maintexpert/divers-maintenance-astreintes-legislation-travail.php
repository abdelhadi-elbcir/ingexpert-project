    <h2>Durée du travail</h2>
	
<p><span style='font-family:"Arial","sans-serif"'>&nbsp;</span></p>

<p class=MsoListParagraph style='margin-left:54.0pt;text-indent:-18.0pt;
line-height:normal'><span style='font-family:Symbol'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span>Dans le cadre de la sous-traitance, certains Donneurs d’Ordres constatent des dépassements des seuils réglementaires en termes de durée du travail journalière ou hebdomadaire. En effet, les Prestataires de Service en maintenance sont particulièrement exposés au non-respect de la législation dans le cadre des astreintes par exemple.
</p>
    <p class=MsoListParagraph style='margin-left:54.0pt;text-indent:-18.0pt;
line-height:normal'><span style='font-family:Symbol'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span>Certains Donneurs d’Ordres ont connaissance du non-respect de la législation du travail de leurs Prestataires de Service, mais pensent à tort que leur propre responsabilité n’est pas engagée. Pour renforcer cette idée de transfert de responsabilité, certains Donneurs d’Ordres adressent un courrier recommandé à leurs Prestataires rappelant l’obligation de respect de la réglementation en prévision d’une démonstration de leur bonne foi. Or, pour éviter tout problème et notamment une implication pénale,
il est fortement recommandé que les Donneurs d’Ordres ne soient pas acteurs dans le non-respect de la réglementation.
    <p class=MsoListParagraph style='margin-left:54.0pt;text-indent:-18.0pt;
line-height:normal'><span style='font-family:Symbol'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span>
Les contrats de prestation de service mal définis peuvent être à l’origine d'une incitation au non-respect de la réglementation. Les Prestataires de Service en maintenance doivent savoir fermement refuser certains engagements contractuels demandés dans un cahier des charges si les moyens en personnel qu’ils doivent mettre en œuvre ne peuvent assurer le respect de la réglementation (sous-effectif, personnel non qualifié, etc). Il est certain que c’est « facile à dire », mais que cela a de lourdes conséquences
financières puisque la perte d’un contrat implique un manque de chiffre d’affaires. Car si un Prestataire de Service refuse un contrat, un autre Prestataire risque de l’accepter. C’est aux Donneurs d’Ordres d’être raisonnables et ne pas être aveuglés par la notion de budget. En cas de non-respect de la réglementation par leurs Prestataires de Service, ils seront co-impliqués. Cela sera très facile à démontrer par exemple dans le cadre des astreintes si c’est eux-mêmes qui donnent un Ordre d’Intervention à un
intervenant qui va ainsi dépasser la durée de travail maximale autorisée.
<p class=MsoListParagraph style='margin-left:54.0pt;text-indent:-18.0pt;
line-height:normal'><span style='font-family:Symbol'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span>Si un Prestataire de Service se rend coupable d’écarts avec la réglementation du travail et sans réaction malgré des avertissements, son Donneur d’Ordres doit savoir mettre fin de façon anticipée à la relation contractuelle. </p> 
