    <ul>
        <h3>1 - Périmètre / découpage fonctionnel</h3>
	

<p class="redaction">

L'AMDEC doit être réalisée  pour une fonction donnée (par exemple lubrifier un palier). On s'appuie sur l'analyse fonctionnelle de cette fonction pour  réaliser un découpage fonctionnel du système  et choisir celui sur lequel se portera l'étude (découpage en niveaux = zoom) et pour lequel on dispose d'historiques.
<br><br>Il faut dresser autant d'arbres fonctionnels qu'il y a d'états du système (mode de fonctionnement, etc). 
<br><br>Rem: il est recommandé de s'appuyer sur un schéma simplifié du système complet pour réaliser l'analyse.
	

  <p style="margin-right:1;">&nbsp;</p>
      


