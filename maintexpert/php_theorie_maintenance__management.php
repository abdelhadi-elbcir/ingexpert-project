<h2>A - La maintenance requiert de la polyvalence</h2>

<p class=MsoNormal><span style='font-family:"Arial","sans-serif"'>&nbsp;</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-family:"Arial","sans-serif"'>Le
groupe constituant le service maintenance a des compétences techniques
diverses, la complémentarité permet de résoudre des problèmes d’origines
diverses. </span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-family:"Arial","sans-serif"'>Métier
«&nbsp;ingrat&nbsp;»&nbsp;: la panne évitée par la maintenance ne vaudra pas de
remerciements puisqu’elle n’a pas existée, par contre la panne réelle vaudra
des critiques.</span></p>


<h2>B - Maintenance dans l'organigramme</h2>

<p class=MsoNormal><span style='font-family:"Arial","sans-serif"'>&nbsp;</span></p>

<p class=MsoNormal align=center style='text-align:center'>
