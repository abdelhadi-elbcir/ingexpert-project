<p style='text-align:justify'><span style='font-size:11.0pt;font-family:"Arial","sans-serif"'>Il
n'y a pas d'organigramme type, toutefois cet organigramme est peut-être à
privilégier par rapport à celui qui suit. Il est peut-être plus adapté aux
petites sociétés (permet d'avoir une personne forte &quot;généraliste&quot;),
mais ne permet pas de suivre une maintenance efficace. Production et
maintenance ont des intérêts totalement différents, il faut bien souvent un
arbitre pour séparer les budgets, les ressources humaines (La production et la
maintenance ont deux façon différentes de vivre l'entreprise). Mais bien
entendu tout dépend des hommes en place.</span></p>

