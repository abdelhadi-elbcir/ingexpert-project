              La formation aborde tous les aspects du management des contrats de sous traitance de la maintenance. Le
              but de la formation est de (re-)définir les relations entre le donneur d'ordre et son sous-traitant et de
              les (re-)contractualiser.
              Toutes les formes de contrats sont prises en compte: <br>
              - les rémunérations de tous type (forfait, bordereau points, dépense contrôlée, ...) <br>
              - les contrats de toutes formes (obligation de résultat ou moyens ou mixés) <br><br>
              Le bon management d'un contrat de sous-traitance doit permettre au donneur d'ordre (contractant) d'avoir
              une prestation de maintenance de bonne efficience: le coût doit être optimum pour un objectif défini.
              Le prestataire sous-traitant doit assurer un reporting juste et précis et doit démontrer qu'il est capable
              de s'améliorer.

              La formation peut être complétée de: <br>
              - un apprentissage à la rédaction des contrats de prestation de service;<br>
              - d'une formation approfondie faite par un juriste. <br><br>


