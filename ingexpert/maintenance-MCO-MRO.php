      </p>


      <!-- Paragraphe 3-->

      <h5>3 - Cas particulier du MRO</h5>

      <p class="redaction">
        Comme nous l’avons vu, le MCO définit le management à déployer pour assurer la fonction requise. Or le MRO
        consiste en l’intervention de maintenance en elle-même, soit une partie du management défini par le MCO.
        <br><br>Le MRO (Maintenance, Repair, and Operations) est donc une partie du MCO (Maintien en Condition
        Opérationnelle). Le MCO est à la charge de l’exploitant du bien. Le MRO peut être sous-traité. Par exemple dans
        l’aéronautique il est assez évident de dire que le MRO est assuré par le fournisseur, le concepteur de
        l’équipement qui en assure inévitablement la maintenance, qui assure le suivi des équipements livrés, leurs
        évolutions et leur maintenance.
        <br><br>Le MRO consiste en le soutien et la maintenance des systèmes livrés à ses clients.



