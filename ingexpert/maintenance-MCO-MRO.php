      <h5>1 - Le Maintien en Condition Opérationelle (MCO)</h5>

      <p class="redaction">
        Le Maintien en Condition Opérationnelle (MCO) est assuré par le management pour assurer à un bien (vous utilisez
        le nom que vous souhaitez : produit, machine, véhicule, équipement, matériel, réseau, infrastructure, système,
        …) d’assurer sa fonction requise. Et ceci tout au long de son <a
          href="https://dev.ingexpert.com/maintexpert/php_theorie_maintenance__lcc_cout_global.php"> Cycle de Vie</a>.
        <br><br>Pour le métier de la maintenance, il n’y a rien de spécifique, il s’agit de déployer un management de la
        maintenance adapté pour maîtriser la fonction requise. Mais cette dernière peut avoir des profils différents :



      <p class="renfoncement1">
        <br><span style="color: orange; "><strong>• </span></strong> la fonction requise initiale, définie lors de la
        livraison du bien, peut évoluer, être calée sur des objectifs qui progressent et donc être supérieure à la
        fonction requise initiale. En effet, les cadences initiales des machines sont souvent dépassées très rapidement
        : dès qu’une cadence est assurée, on cherche à l’augmenter ou à la conserver avec des caractéristiques de
        matières premières plus complexes à maîtriser.
        <br><span style="color: orange; "><strong>• </span></strong> la fonction requise peut ne plus être assurée et il
        s’agit de reconstituer le potentiel du bien. Par exemple reconstituer le potentiel d’heures de vol d’un aéronef
        ou celui d’un char en remplaçant son moteur.
      </p>

      <p class="redaction">
        Il est donc nécessaire de suivre l’évolution de la définition technique des biens au long de leur vie
        opérationnelle. Il s’agit notamment d’assurer la tenue à jour des référentiels techniques, mais aussi l’analyse
        du retour d’expérience issue de l’exploitation des faits techniques.
      </p>


      <!-- Paragraphe 2-->

      <h5>2 - Management du Cycle de Vie </h5>

      <p class="redaction">
        Comme nous l’avons dit, le Maintien en Condition Opérationnelle (MCO) s’applique tout au long du <a
          href="https://dev.ingexpert.com/maintexpert/php_theorie_maintenance__lcc_cout_global.php"> Cycle de Vie</a> du
        bien. Le MCO est donc intégré au management du <a
